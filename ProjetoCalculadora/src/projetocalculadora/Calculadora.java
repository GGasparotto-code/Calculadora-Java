package projetocalculadora;

public class Calculadora {
    float valor1;
    float valor2;
    float resultado;
    
    public void somar() {
        resultado = valor1 + valor2;
    }

    public void subtrair() {
        resultado = valor1 - valor2;
    }

    public void multiplicar() {
        resultado = valor1 * valor2;
    }

    public void dividir() {
        resultado = valor1 / valor2;
    }    
}
